package chap01.section02;

// chap01/section02/Calculator.java
/**
 * 这是一个简单的计算器类，用于执行基本的数学运算。
 */
public class Calculator {

    /**
     * 将两个整数相加并返回结果。
     *
     * @param a 第一个整数
     * @param b 第二个整数
     * @return 两个整数的和
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * 将两个整数相乘并返回结果。
     *
     * @param a 第一个整数
     * @param b 第二个整数
     * @return 两个整数的乘积
     */
    public int multiply(int a, int b) {
        return a * b;
    }
}

